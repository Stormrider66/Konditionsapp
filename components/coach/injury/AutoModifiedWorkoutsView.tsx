'use client'

/**
 * Auto-Modified Workouts Detail View
 *
 * Comprehensive view of all automatically modified workouts with:
 * - Side-by-side original vs modified comparison
 * - Grouping by athlete
 * - 14-day calendar timeline
 * - Bulk approve/reject actions
 * - TSS and zone comparisons
 */

import { useState } from 'react'
import useSWR from 'swr'
import {
  RolePanel as Card,
  RolePanelContent as CardContent,
  RolePanelDescription as CardDescription,
  RolePanelHeader as CardHeader,
  RolePanelTitle as CardTitle,
} from '@/components/layouts/role-shell/RolePage'
import { Button } from '@/components/ui/button'
import { Badge } from '@/components/ui/badge'
import { Checkbox } from '@/components/ui/checkbox'
import { Alert, AlertDescription } from '@/components/ui/alert'
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select'
import {
  AlertDialog,
  AlertDialogAction,
  AlertDialogCancel,
  AlertDialogContent,
  AlertDialogDescription,
  AlertDialogFooter,
  AlertDialogHeader,
  AlertDialogTitle,
} from '@/components/ui/alert-dialog'
import {
  CheckCircle2,
  XCircle,
  ArrowRight,
  Calendar,
  Clock,
  TrendingDown,
  Activity,
  AlertTriangle,
} from 'lucide-react'
import { formatDistanceToNow } from 'date-fns'
import { enUS, sv } from 'date-fns/locale'
import { useToast } from '@/hooks/use-toast'
import { useLocale } from '@/i18n/client'

interface ModifiedWorkout {
  id: string
  clientId: string
  clientName: string
  workoutDate: Date
  originalType: string
  currentType: string
  action: 'CANCEL' | 'REDUCE_INTENSITY' | 'REDUCE_VOLUME' | 'CONVERT_TO_CROSS_TRAINING'
  reasoning: string
  reviewed: boolean
  autoGenerated: boolean
  modifiedAt: Date
  status: string
  originalDuration?: number
  currentDuration?: number
  originalIntensity?: string
  currentIntensity?: string
  originalZones?: string
  currentZones?: string
}

interface GroupedModifications {
  [clientName: string]: ModifiedWorkout[]
}

const fetcher = (url: string) => fetch(url).then(r => r.json())
type AppLocale = 'en' | 'sv'

const getAppLocale = (locale: string): AppLocale => (locale === 'sv' ? 'sv' : 'en')
const localizedText = (locale: AppLocale, svText: string, enText: string) =>
  locale === 'sv' ? svText : enText

export function AutoModifiedWorkoutsView() {
  const { toast } = useToast()
  const locale = getAppLocale(useLocale())
  const t = (svText: string, enText: string) => localizedText(locale, svText, enText)
  const [selectedAthlete, setSelectedAthlete] = useState<string>('ALL')
  const [selectedModifications, setSelectedModifications] = useState<Set<string>>(new Set())
  const [showBulkApproveDialog, setShowBulkApproveDialog] = useState(false)
  const [bulkApproveTarget, setBulkApproveTarget] = useState<'all' | 'athlete' | null>(null)
  const [isApproving, setIsApproving] = useState(false)

  const { data, error, mutate } = useSWR('/api/workouts/modifications', fetcher, {
    refreshInterval: 30000,
  })

  const modifications: ModifiedWorkout[] = data?.modifications || []

  // Group modifications by athlete
  const groupedModifications: GroupedModifications = modifications.reduce((acc, mod) => {
    if (!acc[mod.clientName]) {
      acc[mod.clientName] = []
    }
    acc[mod.clientName].push(mod)
    return acc
  }, {} as GroupedModifications)

  // Get unique athlete names for filter
  const athleteNames = Object.keys(groupedModifications).sort()

  // Filter modifications based on selected athlete
  const filteredModifications =
    selectedAthlete === 'ALL'
      ? modifications
      : modifications.filter(m => m.clientName === selectedAthlete)

  // Count unreviewed
  const unreviewedCount = filteredModifications.filter(m => !m.reviewed).length
  const selectedCount = selectedModifications.size

  // Toggle selection
  const toggleSelection = (id: string) => {
    const newSelection = new Set(selectedModifications)
    if (newSelection.has(id)) {
      newSelection.delete(id)
    } else {
      newSelection.add(id)
    }
    setSelectedModifications(newSelection)
  }

  // Select all for athlete
  const selectAllForAthlete = (athleteName: string) => {
    const athleteMods = groupedModifications[athleteName] || []
    const newSelection = new Set(selectedModifications)
    athleteMods.forEach(mod => {
      if (!mod.reviewed) {
        newSelection.add(mod.id)
      }
    })
    setSelectedModifications(newSelection)
  }

  // Clear all selections
  const clearSelections = () => {
    setSelectedModifications(new Set())
  }

  // Bulk approve
  const handleBulkApprove = async () => {
    setIsApproving(true)
    try {
      const idsToApprove =
        bulkApproveTarget === 'all'
          ? filteredModifications.filter(m => !m.reviewed).map(m => m.id)
          : Array.from(selectedModifications)

      // Approve each modification
      for (const id of idsToApprove) {
        await fetch(`/api/workouts/modifications/${id}/review`, {
          method: 'PUT',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ approved: true }),
        })
      }

      toast({
        title: t('Godkända', 'Approved'),
        description: t(
          `${idsToApprove.length} träningspass har godkänts`,
          `${idsToApprove.length} workouts have been approved`
        ),
      })

      clearSelections()
      void mutate()
    } catch (error) {
      console.error('Error approving modifications:', error)
      toast({
        title: t('Fel', 'Error'),
        description: t('Kunde inte godkänna ändringar', 'Could not approve changes'),
        variant: 'destructive',
      })
    } finally {
      setIsApproving(false)
      setShowBulkApproveDialog(false)
      setBulkApproveTarget(null)
    }
  }

  // Individual approve
  const handleIndividualApprove = async (id: string) => {
    try {
      await fetch(`/api/workouts/modifications/${id}/review`, {
        method: 'PUT',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ approved: true }),
      })

      toast({
        title: t('Godkänd', 'Approved'),
        description: t('Träningsändring godkänd', 'Workout change approved'),
      })

      void mutate()
    } catch (error) {
      console.error('Error approving modification:', error)
      toast({
        title: t('Fel', 'Error'),
        description: t('Kunde inte godkänna ändring', 'Could not approve change'),
        variant: 'destructive',
      })
    }
  }

  if (error) {
    return (
      <Alert variant="destructive">
        <AlertDescription>
          {t('Kunde inte ladda träningsändringar', 'Could not load workout changes')}
        </AlertDescription>
      </Alert>
    )
  }

  if (!data) {
    return (
      <Card>
        <CardContent className="pt-6">
          <p className="text-center text-muted-foreground">{t('Laddar...', 'Loading...')}</p>
        </CardContent>
      </Card>
    )
  }

  if (modifications.length === 0) {
    return (
      <Card>
        <CardContent className="pt-6">
          <div className="flex flex-col items-center justify-center gap-2 py-8">
            <CheckCircle2 className="h-12 w-12 text-emerald-500" />
            <p className="text-lg font-medium">
              {t('Inga automatiska ändringar', 'No automatic changes')}
            </p>
            <p className="text-sm text-muted-foreground">
              {t(
                'Inga träningspass har modifierats automatiskt',
                'No workouts have been modified automatically'
              )}
            </p>
          </div>
        </CardContent>
      </Card>
    )
  }

  return (
    <div className="space-y-6">
      {/* Header with Filters and Actions */}
      <div className="flex items-center justify-between gap-4">
        <div className="flex items-center gap-4">
          <div>
            <h3 className="text-2xl font-bold">
              {t('Auto-modifierade träningspass', 'Auto-modified workouts')}
            </h3>
            <p className="text-sm text-muted-foreground">
              {t(
                `${unreviewedCount} ogranskade av ${filteredModifications.length} totalt`,
                `${unreviewedCount} unreviewed of ${filteredModifications.length} total`
              )}
            </p>
          </div>

          <Select value={selectedAthlete} onValueChange={setSelectedAthlete}>
            <SelectTrigger className="w-[200px]">
              <SelectValue placeholder={t('Alla atleter', 'All athletes')} />
            </SelectTrigger>
            <SelectContent>
              <SelectItem value="ALL">
                {t('Alla atleter', 'All athletes')} ({modifications.length})
              </SelectItem>
              {athleteNames.map(name => (
                <SelectItem key={name} value={name}>
                  {name} ({groupedModifications[name].length})
                </SelectItem>
              ))}
            </SelectContent>
          </Select>
        </div>

        <div className="flex items-center gap-2">
          {selectedCount > 0 && (
            <>
              <Badge variant="secondary" className="px-3 py-1">
                {t(`${selectedCount} valda`, `${selectedCount} selected`)}
              </Badge>
              <Button variant="outline" size="sm" onClick={clearSelections}>
                {t('Rensa', 'Clear')}
              </Button>
              <Button
                variant="default"
                size="sm"
                onClick={() => {
                  setBulkApproveTarget('athlete')
                  setShowBulkApproveDialog(true)
                }}
              >
                <CheckCircle2 className="mr-2 h-4 w-4" />
                {t('Godkänn valda', 'Approve selected')}
              </Button>
            </>
          )}

          {unreviewedCount > 0 && (
            <Button
              variant="default"
              onClick={() => {
                setBulkApproveTarget('all')
                setShowBulkApproveDialog(true)
              }}
            >
              <CheckCircle2 className="mr-2 h-4 w-4" />
              {t('Godkänn alla', 'Approve all')}
            </Button>
          )}
        </div>
      </div>

      {/* Modifications List - Grouped by Athlete */}
      {selectedAthlete === 'ALL' ? (
        <div className="space-y-6">
          {athleteNames.map(athleteName => {
            const athleteMods = groupedModifications[athleteName]
            const athleteUnreviewed = athleteMods.filter(m => !m.reviewed).length

            return (
              <Card key={athleteName}>
                <CardHeader>
                  <div className="flex items-center justify-between">
                    <div>
                      <CardTitle>{athleteName}</CardTitle>
                      <CardDescription>
                        {t(
                          `${athleteMods.length} modifieringar, ${athleteUnreviewed} ogranskade`,
                          `${athleteMods.length} changes, ${athleteUnreviewed} unreviewed`
                        )}
                      </CardDescription>
                    </div>
                    {athleteUnreviewed > 0 && (
                      <Button
                        variant="outline"
                        size="sm"
                        onClick={() => selectAllForAthlete(athleteName)}
                      >
                        {t('Välj alla', 'Select all')}
                      </Button>
                    )}
                  </div>
                </CardHeader>
                <CardContent className="space-y-4">
                  {athleteMods.map(mod => (
                    <ModificationCard
                      key={mod.id}
                      modification={mod}
                      isSelected={selectedModifications.has(mod.id)}
                      onToggleSelect={() => toggleSelection(mod.id)}
                      onApprove={() => handleIndividualApprove(mod.id)}
                      locale={locale}
                    />
                  ))}
                </CardContent>
              </Card>
            )
          })}
        </div>
      ) : (
        <div className="space-y-4">
          {filteredModifications.map(mod => (
            <ModificationCard
              key={mod.id}
              modification={mod}
              isSelected={selectedModifications.has(mod.id)}
              onToggleSelect={() => toggleSelection(mod.id)}
              onApprove={() => handleIndividualApprove(mod.id)}
              locale={locale}
            />
          ))}
        </div>
      )}

      {/* Bulk Approve Confirmation Dialog */}
      <AlertDialog open={showBulkApproveDialog} onOpenChange={setShowBulkApproveDialog}>
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>{t('Bekräfta godkännande', 'Confirm approval')}</AlertDialogTitle>
            <AlertDialogDescription>
              {bulkApproveTarget === 'all'
                ? t(
                    `Är du säker på att du vill godkänna alla ${unreviewedCount} ogranskade träningsändringar?`,
                    `Are you sure you want to approve all ${unreviewedCount} unreviewed workout changes?`
                  )
                : t(
                    `Är du säker på att du vill godkänna ${selectedCount} valda träningsändringar?`,
                    `Are you sure you want to approve ${selectedCount} selected workout changes?`
                  )}
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel>{t('Avbryt', 'Cancel')}</AlertDialogCancel>
            <AlertDialogAction onClick={handleBulkApprove} disabled={isApproving}>
              {isApproving ? t('Godkänner...', 'Approving...') : t('Godkänn', 'Approve')}
            </AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>
    </div>
  )
}

/**
 * Individual Modification Card with Comparison View
 */
function ModificationCard({
  modification,
  isSelected,
  onToggleSelect,
  onApprove,
  locale,
}: {
  modification: ModifiedWorkout
  isSelected: boolean
  onToggleSelect: () => void
  onApprove: () => void
  locale: AppLocale
}) {
  const t = (svText: string, enText: string) => localizedText(locale, svText, enText)
  const dateFnsLocale = locale === 'sv' ? sv : enUS
  const browserLocale = locale === 'sv' ? 'sv-SE' : 'en-US'
  const actionLabels = {
    CANCEL: t('Avbokad', 'Canceled'),
    REDUCE_INTENSITY: t('Reducerad intensitet', 'Reduced intensity'),
    REDUCE_VOLUME: t('Reducerad volym', 'Reduced volume'),
    CONVERT_TO_CROSS_TRAINING: t(
      'Konverterad till kompletterande träning',
      'Converted to cross-training'
    ),
  }

  const actionColors = {
    CANCEL: 'destructive',
    REDUCE_INTENSITY: 'default',
    REDUCE_VOLUME: 'default',
    CONVERT_TO_CROSS_TRAINING: 'secondary',
  } as const

  const actionIcons = {
    CANCEL: XCircle,
    REDUCE_INTENSITY: TrendingDown,
    REDUCE_VOLUME: TrendingDown,
    CONVERT_TO_CROSS_TRAINING: Activity,
  }

  const ActionIcon = actionIcons[modification.action]

  // Calculate volume reduction %
  const volumeReduction =
    modification.originalDuration && modification.currentDuration
      ? Math.round(
          ((modification.originalDuration - modification.currentDuration) /
            modification.originalDuration) *
            100
        )
      : null

  return (
    <Card
      className={`${!modification.reviewed ? 'border-blue-200 bg-blue-50/30' : ''} ${
        isSelected ? 'ring-2 ring-blue-500' : ''
      }`}
    >
      <CardContent className="pt-6">
        <div className="space-y-4">
          {/* Header Row */}
          <div className="flex items-start justify-between">
            <div className="flex items-start gap-3">
              {!modification.reviewed && (
                <Checkbox checked={isSelected} onCheckedChange={onToggleSelect} className="mt-1" />
              )}
              <div>
                <div className="flex items-center gap-2">
                  <Calendar className="h-4 w-4 text-muted-foreground" />
                  <p className="font-semibold">
                    {new Date(modification.workoutDate).toLocaleDateString(browserLocale, {
                      weekday: 'long',
                      day: 'numeric',
                      month: 'long',
                    })}
                  </p>
                  {modification.autoGenerated && (
                    <Badge variant="outline" className="text-xs">
                      Auto
                    </Badge>
                  )}
                </div>
                <p className="text-sm text-muted-foreground">
                  {t('Modifierad', 'Modified')}{' '}
                  {formatDistanceToNow(new Date(modification.modifiedAt), {
                    addSuffix: true,
                    locale: dateFnsLocale,
                  })}
                </p>
              </div>
            </div>

            <Badge variant={actionColors[modification.action]}>
              <ActionIcon className="mr-1 h-3 w-3" />
              {actionLabels[modification.action]}
            </Badge>
          </div>

          {/* Comparison View */}
          <div className="grid grid-cols-[1fr_auto_1fr] gap-4 items-start">
            {/* Original Workout */}
            <div className="space-y-2">
              <p className="text-xs font-medium text-muted-foreground uppercase">Original</p>
              <div className="p-3 bg-muted/50 rounded-lg space-y-1">
                <p className="font-semibold">{modification.originalType}</p>
                {modification.originalDuration && (
                  <div className="flex items-center gap-1 text-sm text-muted-foreground">
                    <Clock className="h-3 w-3" />
                    {modification.originalDuration} min
                  </div>
                )}
                {modification.originalIntensity && (
                  <p className="text-sm">
                    <span className="font-medium">{t('Intensitet:', 'Intensity:')}</span>{' '}
                    {modification.originalIntensity}
                  </p>
                )}
                {modification.originalZones && (
                  <p className="text-sm">
                    <span className="font-medium">{t('Zoner:', 'Zones:')}</span>{' '}
                    {modification.originalZones}
                  </p>
                )}
              </div>
            </div>

            {/* Arrow */}
            <div className="flex items-center justify-center pt-8">
              <ArrowRight className="h-6 w-6 text-muted-foreground" />
            </div>

            {/* Modified Workout */}
            <div className="space-y-2">
              <p className="text-xs font-medium text-muted-foreground uppercase">
                {t('Modifierad', 'Modified')}
              </p>
              <div className="p-3 bg-blue-50 border border-blue-200 rounded-lg space-y-1">
                <p className="font-semibold">{modification.currentType}</p>
                {modification.currentDuration && (
                  <div className="flex items-center gap-1 text-sm text-muted-foreground">
                    <Clock className="h-3 w-3" />
                    {modification.currentDuration} min
                    {volumeReduction !== null && (
                      <span className="text-red-600 font-medium">(-{volumeReduction}%)</span>
                    )}
                  </div>
                )}
                {modification.currentIntensity && (
                  <p className="text-sm">
                    <span className="font-medium">{t('Intensitet:', 'Intensity:')}</span>{' '}
                    {modification.currentIntensity}
                  </p>
                )}
                {modification.currentZones && (
                  <p className="text-sm">
                    <span className="font-medium">{t('Zoner:', 'Zones:')}</span>{' '}
                    {modification.currentZones}
                  </p>
                )}
              </div>
            </div>
          </div>

          {/* Reasoning */}
          <Alert>
            <AlertTriangle className="h-4 w-4" />
            <AlertDescription>
              <p className="font-medium mb-1">{t('Orsak:', 'Reason:')}</p>
              <p className="text-sm">{modification.reasoning}</p>
            </AlertDescription>
          </Alert>

          {/* Actions */}
          {!modification.reviewed && (
            <div className="flex items-center justify-end gap-2 pt-2 border-t">
              <Button variant="outline" size="sm">
                <XCircle className="mr-2 h-4 w-4" />
                {t('Underkänn', 'Reject')}
              </Button>
              <Button variant="default" size="sm" onClick={onApprove}>
                <CheckCircle2 className="mr-2 h-4 w-4" />
                {t('Godkänn', 'Approve')}
              </Button>
            </div>
          )}

          {modification.reviewed && (
            <div className="flex items-center justify-center gap-2 pt-2 border-t">
              <CheckCircle2 className="h-4 w-4 text-emerald-500" />
              <p className="text-sm text-muted-foreground">
                {t('Granskad och godkänd', 'Reviewed and approved')}
              </p>
            </div>
          )}
        </div>
      </CardContent>
    </Card>
  )
}
