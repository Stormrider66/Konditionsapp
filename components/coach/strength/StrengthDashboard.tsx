'use client'

import React, { useEffect, useState } from 'react'
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card'
import { Tabs, TabsContent, TabsList, TabsTrigger } from '@/components/ui/tabs'
import { Dumbbell, Activity, Calendar, Library, Plus, FolderOpen, Wand2, TrendingUp, TrendingDown } from 'lucide-react'
import { Button } from '@/components/ui/button'
import { Skeleton } from '@/components/ui/skeleton'
import dynamic from 'next/dynamic'

const ExerciseLibraryBrowser = dynamic(
  () => import('@/components/coach/exercise-library/ExerciseLibraryBrowser').then(mod => mod.ExerciseLibraryBrowser),
  { ssr: false, loading: () => <div className="p-8 text-center text-muted-foreground">Laddar övningsbibliotek...</div> }
)
import { ExerciseCreator } from './ExerciseCreator'
import { SessionBuilder } from './SessionBuilder'
import { SectionWorkoutBuilder } from './SectionWorkoutBuilder'
import { StrengthSessionLibrary } from './StrengthSessionLibrary'
import { AutoGenerateDialog } from './AutoGenerateDialog'
import type { StrengthSessionData } from '@/types'

interface GeneratedSession {
  name: string
  description: string
  phase: string
  estimatedDuration: number
  sections: Array<{
    type: string
    exercises: Array<{
      exerciseId: string
      exerciseName: string
      sets: number
      reps: number | string
      weight?: number
      restSeconds?: number
      tempo?: string
      notes?: string
    }>
    notes?: string
    duration?: number
  }>
  totalExercises: number
  totalSets: number
}

interface StrengthStats {
  totalExercises: number
  exercisesAddedThisWeek: number
  activePrograms: number
  athletesWithPrograms: number
  complianceRate: number
  complianceChange: string
  prsThisWeek: number
  topPrExercises: string | null
}

interface StrengthDashboardProps {
  businessId?: string
}

export function StrengthDashboard({ businessId }: StrengthDashboardProps) {
  const [showCreator, setShowCreator] = React.useState(false)
  const [activeTab, setActiveTab] = React.useState('library')
  const [editSession, setEditSession] = React.useState<StrengthSessionData | null>(null)
  const [useSectionBuilder, setUseSectionBuilder] = React.useState(true) // Default to section builder
  const [stats, setStats] = useState<StrengthStats | null>(null)
  const [statsLoading, setStatsLoading] = useState(true)

  // Fetch stats on mount
  useEffect(() => {
    async function fetchStats() {
      try {
        // Use business-scoped API if businessId is provided
        const url = businessId
          ? `/api/business/${businessId}/strength/stats`
          : '/api/coach/strength/stats'
        const res = await fetch(url)
        if (res.ok) {
          const data = await res.json()
          setStats(data)
        }
      } catch (error) {
        console.error('Failed to fetch strength stats:', error)
      } finally {
        setStatsLoading(false)
      }
    }
    fetchStats()
  }, [businessId])

  // Handle auto-generated session
  const handleAutoGenerated = (session: GeneratedSession) => {
    // Convert generated session to editable format for SectionWorkoutBuilder
    const warmupSection = session.sections.find((s) => s.type === 'WARMUP')
    const mainSection = session.sections.find((s) => s.type === 'MAIN')
    const coreSection = session.sections.find((s) => s.type === 'CORE')
    const cooldownSection = session.sections.find((s) => s.type === 'COOLDOWN')

    const editableSession: StrengthSessionData = {
      name: session.name,
      description: session.description,
      phase: session.phase,
      exercises: (mainSection?.exercises || []).map((e) => ({
        exerciseId: e.exerciseId,
        exerciseName: e.exerciseName,
        sets: e.sets,
        reps: typeof e.reps === 'number' ? e.reps : parseInt(e.reps as string) || 10,
        weight: e.weight,
        restSeconds: e.restSeconds,
        notes: e.notes,
      })),
      warmupData: warmupSection
        ? {
            notes: warmupSection.notes,
            duration: warmupSection.duration,
            exercises: warmupSection.exercises.map((e) => ({
              exerciseId: e.exerciseId,
              exerciseName: e.exerciseName,
              sets: e.sets,
              reps: e.reps,
              notes: e.notes,
            })),
          }
        : undefined,
      coreData: coreSection
        ? {
            notes: coreSection.notes,
            duration: coreSection.duration,
            exercises: coreSection.exercises.map((e) => ({
              exerciseId: e.exerciseId,
              exerciseName: e.exerciseName,
              sets: e.sets,
              reps: e.reps,
              restSeconds: e.restSeconds,
              notes: e.notes,
            })),
          }
        : undefined,
      cooldownData: cooldownSection
        ? {
            notes: cooldownSection.notes,
            duration: cooldownSection.duration,
            exercises: cooldownSection.exercises.map((e) => ({
              exerciseId: e.exerciseId,
              exerciseName: e.exerciseName,
              sets: e.sets || 1,
              reps: e.reps || '30s',
              duration: typeof e.reps === 'number' ? e.reps : 30,
              notes: e.notes,
            })),
          }
        : undefined,
    }

    setEditSession(editableSession)
    setUseSectionBuilder(true)
    setActiveTab('builder')
  }

  if (showCreator) {
    return (
      <div className="container mx-auto py-6">
        <Button 
          variant="ghost" 
          onClick={() => setShowCreator(false)}
          className="mb-4"
        >
          ← Back to Dashboard
        </Button>
        <ExerciseCreator onClose={() => setShowCreator(false)} />
      </div>
    )
  }

  return (
    <div className="container mx-auto py-6 space-y-8">
      <div className="flex justify-between items-center">
        <div>
          <h1 className="text-3xl font-bold tracking-tight">Strength Studio</h1>
          <p className="text-muted-foreground mt-2">
            Manage exercises, build sophisticated strength programs, and track athlete progression.
          </p>
        </div>
        <div className="flex gap-2">
          <AutoGenerateDialog
            onGenerated={handleAutoGenerated}
            trigger={
              <Button variant="outline">
                <Wand2 className="mr-2 h-4 w-4" />
                Auto-generera
              </Button>
            }
          />
          <Button onClick={() => setShowCreator(true)}>
            <Plus className="mr-2 h-4 w-4" />
            New Exercise
          </Button>
        </div>
      </div>

      <div className="grid gap-4 md:grid-cols-2 lg:grid-cols-4">
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Övningar</CardTitle>
            <Dumbbell className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            {statsLoading ? (
              <Skeleton className="h-8 w-16" />
            ) : (
              <>
                <div className="text-2xl font-bold">{stats?.totalExercises ?? 0}</div>
                <p className="text-xs text-muted-foreground">
                  {stats?.exercisesAddedThisWeek ? `+${stats.exercisesAddedThisWeek} denna vecka` : 'Tillgängliga övningar'}
                </p>
              </>
            )}
          </CardContent>
        </Card>
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Aktiva pass</CardTitle>
            <Activity className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            {statsLoading ? (
              <Skeleton className="h-8 w-16" />
            ) : (
              <>
                <div className="text-2xl font-bold">{stats?.activePrograms ?? 0}</div>
                <p className="text-xs text-muted-foreground">
                  {stats?.athletesWithPrograms ? `Till ${stats.athletesWithPrograms} atleter` : 'Inga tilldelade pass'}
                </p>
              </>
            )}
          </CardContent>
        </Card>
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Genomförande</CardTitle>
            <Calendar className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            {statsLoading ? (
              <Skeleton className="h-8 w-16" />
            ) : (
              <>
                <div className="text-2xl font-bold">{stats?.complianceRate ?? 0}%</div>
                <p className="text-xs text-muted-foreground flex items-center gap-1">
                  {stats?.complianceChange && parseFloat(stats.complianceChange) !== 0 ? (
                    <>
                      {parseFloat(stats.complianceChange) > 0 ? (
                        <TrendingUp className="h-3 w-3 text-green-500" />
                      ) : (
                        <TrendingDown className="h-3 w-3 text-red-500" />
                      )}
                      {parseFloat(stats.complianceChange) > 0 ? '+' : ''}{stats.complianceChange}% från förra månaden
                    </>
                  ) : (
                    'Denna månad'
                  )}
                </p>
              </>
            )}
          </CardContent>
        </Card>
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">PR denna vecka</CardTitle>
            <TrendingUp className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            {statsLoading ? (
              <Skeleton className="h-8 w-16" />
            ) : (
              <>
                <div className="text-2xl font-bold">{stats?.prsThisWeek ?? 0}</div>
                <p className="text-xs text-muted-foreground">
                  {stats?.topPrExercises || 'Inga PR registrerade'}
                </p>
              </>
            )}
          </CardContent>
        </Card>
      </div>

      <Tabs value={activeTab} onValueChange={setActiveTab} className="space-y-4">
        <TabsList>
          <TabsTrigger value="library" className="flex items-center gap-2">
            <Library className="h-4 w-4" />
            Övningar
          </TabsTrigger>
          <TabsTrigger value="sessions" className="flex items-center gap-2">
            <FolderOpen className="h-4 w-4" />
            Passbibliotek
          </TabsTrigger>
          <TabsTrigger value="builder" className="flex items-center gap-2">
            <Calendar className="h-4 w-4" />
            Skapa Pass
          </TabsTrigger>
          <TabsTrigger value="progression" className="flex items-center gap-2">
            <Activity className="h-4 w-4" />
            Progression
          </TabsTrigger>
        </TabsList>

        <TabsContent value="library" className="space-y-4">
          <ExerciseLibraryBrowser mode="browse" />
        </TabsContent>

        <TabsContent value="sessions" className="space-y-4">
          <StrengthSessionLibrary
            onNewSession={() => {
              setEditSession(null)
              setActiveTab('builder')
            }}
            onEditSession={(session) => {
              setEditSession(session)
              setActiveTab('builder')
            }}
            businessId={businessId}
          />
        </TabsContent>

        <TabsContent value="builder">
          <div className="mb-4 flex items-center justify-between">
            <div className="flex items-center gap-4">
              <Button
                variant={useSectionBuilder ? 'default' : 'outline'}
                size="sm"
                onClick={() => setUseSectionBuilder(true)}
              >
                Sektionsbyggare
              </Button>
              <Button
                variant={!useSectionBuilder ? 'default' : 'outline'}
                size="sm"
                onClick={() => setUseSectionBuilder(false)}
              >
                Enkel byggare
              </Button>
            </div>
            {useSectionBuilder && (
              <p className="text-sm text-muted-foreground">
                Bygg pass med uppvärmning, huvudpass, core och nedvarvning
              </p>
            )}
          </div>

          {useSectionBuilder ? (
            <SectionWorkoutBuilder
              initialData={editSession}
              onSaved={() => {
                setEditSession(null)
                setActiveTab('sessions')
              }}
              onCancel={editSession ? () => {
                setEditSession(null)
                setActiveTab('sessions')
              } : undefined}
            />
          ) : (
            <SessionBuilder
              initialData={editSession}
              onSaved={() => {
                setEditSession(null)
                setActiveTab('sessions')
              }}
              onCancel={editSession ? () => {
                setEditSession(null)
                setActiveTab('sessions')
              } : undefined}
            />
          )}
        </TabsContent>

        <TabsContent value="progression">
           <Card>
            <CardHeader>
              <CardTitle>Progression Tracking</CardTitle>
              <CardDescription>View athlete 1RM trends and volume loads.</CardDescription>
            </CardHeader>
            <CardContent className="h-[400px] flex items-center justify-center border-dashed border-2 rounded-lg m-4">
              <p className="text-muted-foreground">Progression Charts Coming Soon</p>
            </CardContent>
          </Card>
        </TabsContent>
      </Tabs>
    </div>
  )
}

