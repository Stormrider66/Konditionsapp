async function main() {
  console.log('üèãÔ∏è Seeding comprehensive exercise library based on scientific framework...\n')

  let created = 0
  let skipped = 0

  for (const exercise of exercises) {
    // Check if exercise already exists (by name and coachId=null for system exercises)
    const existing = await prisma.exercise.findFirst({
      where: { name: exercise.name, coachId: null },
    })

    if (existing) {
      console.log(`‚è≠Ô∏è  Skipped (exists): ${exercise.name}`)
      skipped++
      continue
    }

    const createdExercise = await prisma.exercise.create({
      data: {
        ...exercise,
        nameSv: exercise.nameSv || exercise.name,
      },
    })
    console.log(`‚úÖ Created: ${createdExercise.name} (${createdExercise.biomechanicalPillar || createdExercise.category})`)
    created++
  }

  console.log('\n' + '='.repeat(60))
  console.log('üìä Exercise Library Seeding Summary')
  console.log('='.repeat(60))
  console.log(`‚úÖ Created: ${created} exercises`)
  console.log(`‚è≠Ô∏è  Skipped: ${skipped} exercises (already existed)`)
  console.log(`üìö Total in library: ${created + skipped} exercises`)
  console.log('='.repeat(60))

  // Summary by category
  const summary = exercises.reduce((acc: any, ex) => {
    const pillar = ex.biomechanicalPillar || 'OTHER'
    acc[pillar] = (acc[pillar] || 0) + 1
    return acc
  }, {})

  console.log('\nüìã Breakdown by Biomechanical Pillar:')
  Object.entries(summary).forEach(([pillar, count]) => {
    console.log(`   ${pillar}: ${count} exercises`)
  })

  console.log('\n‚ú® Exercise library ready for Norwegian Singles integration!')
}

main()
  .catch((e) => {
    console.error('‚ùå Error seeding exercises:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
