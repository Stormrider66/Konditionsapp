  console.log(`üì¶ Processing ${strengthExercises.length} strength exercises...`)

  let created = 0
  let updated = 0
  let skipped = 0

  // Process all exercises
  for (const exercise of strengthExercises) {
    const existing = await prisma.exercise.findFirst({
      where: { name: exercise.name, coachId: null },
    })

    if (existing) {
      // Update existing to ensure it has both flags
      if (!existing.isHybridMovement || !existing.biomechanicalPillar) {
        await prisma.exercise.update({
          where: { id: existing.id },
          data: {
            isHybridMovement: true,
            biomechanicalPillar: exercise.biomechanicalPillar,
            progressionLevel: exercise.progressionLevel || existing.progressionLevel,
            movementCategory: exercise.movementCategory || existing.movementCategory,
            equipmentTypes: exercise.equipmentTypes || existing.equipmentTypes,
            defaultReps: exercise.defaultReps || existing.defaultReps,
            defaultWeightMale: exercise.defaultWeightMale || existing.defaultWeightMale,
            defaultWeightFemale: exercise.defaultWeightFemale || existing.defaultWeightFemale,
            scaledWeightMale: exercise.scaledWeightMale || existing.scaledWeightMale,
            scaledWeightFemale: exercise.scaledWeightFemale || existing.scaledWeightFemale,
            standardAbbreviation: exercise.standardAbbreviation || existing.standardAbbreviation,
          },
        })
        updated++
        console.log(`  ‚úèÔ∏è  Updated: ${exercise.name}`)
      } else {
        skipped++
      }
    } else {
      // Create new exercise
      await prisma.exercise.create({
        data: {
          name: exercise.name,
          nameSv: exercise.nameSv,
          nameEn: exercise.nameEn,
          category: exercise.category,
          muscleGroup: exercise.muscleGroup,
          biomechanicalPillar: exercise.biomechanicalPillar,
          progressionLevel: exercise.progressionLevel,
          plyometricIntensity: exercise.plyometricIntensity,
          contactsPerRep: exercise.contactsPerRep,
          movementCategory: exercise.movementCategory,
          equipmentTypes: exercise.equipmentTypes || [],
          description: exercise.description,
          instructions: exercise.instructions,
          equipment: exercise.equipment,
          difficulty: exercise.difficulty,
          videoUrl: exercise.videoUrl,
          isPublic: true,
          isHybridMovement: true,
          defaultReps: exercise.defaultReps,
          defaultWeightMale: exercise.defaultWeightMale,
          defaultWeightFemale: exercise.defaultWeightFemale,
          scaledWeightMale: exercise.scaledWeightMale,
          scaledWeightFemale: exercise.scaledWeightFemale,
          foundationMovement: exercise.foundationMovement,
          standardAbbreviation: exercise.standardAbbreviation,
        },
      })
      created++
      console.log(`  ‚úÖ Created: ${exercise.name}`)
    }
  }

  // Now update ALL existing exercises to have isHybridMovement = true
  console.log('\nüîÑ Updating all existing exercises with isHybridMovement = true...')
  const updateResult = await prisma.exercise.updateMany({
    where: { isHybridMovement: false },
    data: { isHybridMovement: true },
  })
  console.log(`  ‚úÖ Updated ${updateResult.count} exercises with isHybridMovement flag`)

  // Update exercises missing biomechanicalPillar based on their properties
  console.log('\nüîÑ Adding biomechanicalPillar to exercises that are missing it...')
  const exercisesWithoutPillar = await prisma.exercise.findMany({
    where: { biomechanicalPillar: null },
  })

  let pillarUpdated = 0
  for (const ex of exercisesWithoutPillar) {
    const pillar = getBiomechanicalPillar(
      ex.movementCategory || '',
      ex.muscleGroup || '',
      ex.name
    )
    await prisma.exercise.update({
      where: { id: ex.id },
      data: { biomechanicalPillar: pillar },
    })
    pillarUpdated++
  }
  console.log(`  ‚úÖ Added biomechanicalPillar to ${pillarUpdated} exercises`)

  // Summary
  const totalCount = await prisma.exercise.count({ where: { coachId: null } })
  const hybridCount = await prisma.exercise.count({ where: { isHybridMovement: true, coachId: null } })
